"use strict"

/* Redondeo Automático con metodo round */
var numero1 = 4.5;
var numero1Red = Math.round(numero1);
console.log(numero1Red);

/* Metodo floor y método ceil */
var numero2 = 7.9;
var numero2Floor = Math.floor(numero2);
console.log(numero2Floor);

var numero3 = 9.1;
var numero3Ceil = Math.ceil(numero3);
console.log(numero3Ceil);